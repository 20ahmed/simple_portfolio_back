const mongoose = require("mongoose");

const worksSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  link: {
    type: String,
    required: true,
  },
});

const Work = mongoose.model("Work", worksSchema);

module.exports = Work;
